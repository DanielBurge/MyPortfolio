
public class WrapperClasses {
	public static void main(String[] args) {
		// wrapper class = provides a way to use data types as reference data types
		// can be used with ArrayList (collections)
		
		// primitive   // wrapper
		//
		// boolean      Boolean
		// double       Double 
		// char          Character 
		// int            Integer       
		
		//autoboxing = automatic conversion that Java compiler makes between primitive types and their wrapper class
		// unboxing = the reverse of autoboxing. Automatic conversion of wrapper class to primitive.
	
	Boolean a = true;
	Character b = '@';
	Integer c = 24;
	Double d = 3.14;
	String e = "Bro";
	
	if(b=='@') {
		System.out.println("This is true");
	}
	
	
	
	
	}

}
